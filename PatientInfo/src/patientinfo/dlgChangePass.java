    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if (new String(passOld.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Old password!");
        } else if (new String(passNew.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter new password!");
        } else if (new String(passConfirm.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Confirm password!");
        } else if (!new String(passOld.getPassword()).equals(password)) {
            JOptionPane.showMessageDialog(this, "Old password valid!!");
        } else if(new String(passNew.getPassword()).length()<4 || new String(passConfirm.getPassword()).length()<4){
            JOptionPane.showMessageDialog(this, "Password length must be greater than 4 characters");
        } else if (!new String(passNew.getPassword()).equals(new String(passConfirm.getPassword()))) {
            JOptionPane.showMessageDialog(this, "New password don't like Confirm password!!");
        } else {
            changePass();
        }
    }//GEN-LAST:event_btnOkActionPerformed

