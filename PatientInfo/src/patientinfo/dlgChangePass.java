    public void changePass(){
        ResultSet rs = null;
        try {
            String sSelect = "Select password from tblAccount where account = ? ";
            PreparedStatement pstmt = conn.prepareStatement(sSelect);
            pstmt.setString(1, account);
            rs = pstmt.executeQuery();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        try {
            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "This is account default! Don't change password!");
            } else {
                try {
                    if (rs.getString(1).equals(new String(passOld.getPassword()))) {
                        String sUpdate = "UPDATE tblAccount SET password  = ? where Account = ? ";
                        PreparedStatement pstmt1 = conn.prepareStatement(sUpdate);
                        pstmt1.setString(1, new String(passConfirm.getPassword()));
                        pstmt1.setString(2, account);
                        pstmt1.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Change password success!");
                    }
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

